package br.com.fangames.desenvolvedor;
import br.com.fangames.game.Game;

import java.io.Serializable;
import java.util.List;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToMany;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import javax.validation.constraints.Size;

@Entity
@Table(name="tb_desenvolvedor")
public class Desenvolvedor implements Serializable{
	

	private static final long serialVersionUID = -5283465718340396164L;

	@Id
	@SequenceGenerator(name="nm_seq_desenvolvedor", sequenceName="seq_desenvolvedor", allocationSize=1,initialValue=1)
	@GeneratedValue(generator="nm_seq_desenvolvedor",strategy=GenerationType.SEQUENCE)
	@Column(name="id_desenvolvedor")
	private Integer id;
	
	@Column(name="nm_desenvolvedor",unique=true,length=50)
	@Size(max=50,min=1)
	private String nome;
	
	@OneToMany(mappedBy="desenvolvedor")
	private List<Game> games;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome.trim();
	}

	public List<Game> getGames() {
		return games;
	}

	public void setGames(List<Game> games) {
		this.games = games;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((games == null) ? 0 : games.hashCode());
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((nome == null) ? 0 : nome.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Desenvolvedor other = (Desenvolvedor) obj;
		if (games == null) {
			if (other.games != null)
				return false;
		} else if (!games.equals(other.games))
			return false;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (nome == null) {
			if (other.nome != null)
				return false;
		} else if (!nome.equals(other.nome))
			return false;
		return true;
	}


	
	

}
